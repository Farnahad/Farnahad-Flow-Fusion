namespace FarnahadFlowFusion.Action.Loops.LoopConditionBase;

public enum Operator
{
    /// <summary>
    /// Equal to (=)
    /// </summary>
    EqualTo,
    /// <summary>
    /// Grater than (>)
    /// </summary>
    GraterThan,
    /// <summary>
    /// Grater than or equal to (>=)
    /// </summary>
    GraterThanOrEqualTo,
    /// <summary>
    /// Less than (<)
    /// </summary>
    LessThan,
    /// <summary>
    /// Less than or equal to (<=)
    /// </summary>
    LessThanOrEqualTo,
    /// <summary>
    /// Not Equal To (<>)
    /// </summary>
    NotEqualTo
}